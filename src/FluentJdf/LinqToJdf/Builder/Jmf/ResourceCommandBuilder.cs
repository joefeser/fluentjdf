
using System.Xml.Linq;
using Infrastructure.Core.CodeContracts;

namespace FluentJdf.LinqToJdf.Builder.Jmf {
	/// <summary>
	/// Used to build Resource
	/// </summary>
	public partial class ResourceCommandBuilder : CommandBuilder {
		internal const string IdPrefix = "R_";

		internal ResourceCommandBuilder(JmfNodeBuilder parent)
			: base(parent, Command.Resource, IdPrefix) {
			ParameterCheck.ParameterRequired(parent, "parent");
		}

		/// <summary>
		/// Add a non JDF Element to the Command.
		/// </summary>
		/// <param name="element">The element to add.</param>
		/// <returns></returns>
		public ResourceCommandBuilder AddNode(XElement element) {
			ParameterCheck.ParameterRequired(element, "element");
			ParentJmfNode.Element.Add(element);
			return this;
		}

		/// <summary>
		/// Gets the attribute builder.
		/// </summary>
		/// <returns></returns>
		public ResourceCommandAttributeBuilder With() {
			return new ResourceCommandAttributeBuilder(this);
		}
	}
}
